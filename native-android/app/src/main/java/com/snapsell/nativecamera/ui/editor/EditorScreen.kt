// Per-photo edit state
data class PhotoAdjustments(
    val exposure: Float = 0f,
    val contrast: Float = 0f,
    val scale: Float = 1f,
    val appliedFilter: AiFilter? = null
)

enum class AiFilter(val label: String, val prompt: String, val color: Color) {
    ENHANCE("Enhance", "Enhance this product photo for an online store. Improve lighting, sharpness, and color balance while keeping it natural.", Color(0xFF60A5FA)),
    WHITE_BG("White BG", "Remove the background and replace it with a clean, professional studio white background.", Color(0xFF34D399)),
    LIFESTYLE("Lifestyle", "Place this item in a professional lifestyle setting that would appeal to online shoppers.", Color(0xFFC084FC))
}

enum class EditorTab(val label: String) {
    ADJUST("Adjust"),
    AI_FILTERS("AI Filters"),
    CROP("Crop")
}

// Toast state
data class ToastState(
    val message: String,
    val isVisible: Boolean = false,
    val isWarning: Boolean = false
)

@Composable
fun EditorScreen(
    photoPaths: List<String>,
    onBack: () -> Unit
) {
    val context = LocalContext.current
    val scope = rememberCoroutineScope()

    // Multi-photo state
    var selectedIndex by remember { mutableIntStateOf(0) }
    val adjustments = remember { mutableStateMapOf<Int, PhotoAdjustments>() }
    var applyToAll by remember { mutableStateOf(false) }

    // Tab state
    var activeTab by remember { mutableStateOf(EditorTab.ADJUST) }

    // AI listing generation
    var generatedTitle by remember { mutableStateOf("") }
    var generatedDescription by remember { mutableStateOf("") }
    var generatedPrice by remember { mutableStateOf("") }
    var isGenerating by remember { mutableStateOf(false) }
    var hasGenerated by remember { mutableStateOf(false) }
    var errorMessage by remember { mutableStateOf<String?>(null) }

    // Item name
    var itemName by remember { mutableStateOf("") }
    var isSuggestingName by remember { mutableStateOf(false) }

    // AI filter processing
    var isFilterProcessing by remember { mutableStateOf(false) }

    // Save state
    var isSaving by remember { mutableStateOf(false) }

    // Toast
    var toastState by remember { mutableStateOf(ToastState("")) }

    // Auto-dismiss toast
    LaunchedEffect(toastState.isVisible) {
        if (toastState.isVisible) {
            val duration = if (toastState.isWarning) 5000L else 3500L
            delay(duration)
            toastState = toastState.copy(isVisible = false)
        }
    }

    // Current photo adjustments
    val currentAdjustments = adjustments[selectedIndex] ?: PhotoAdjustments()

    fun updateAdjustments(newAdj: PhotoAdjustments) {
        adjustments[selectedIndex] = newAdj
        if (applyToAll) {
            photoPaths.indices.forEach { i ->
                if (i != selectedIndex) adjustments[i] = newAdj
            }
        }
    }

    BackHandler(onBack = onBack)

    Box(
        modifier = Modifier
            .fillMaxSize()
            .background(
                Brush.verticalGradient(
                    colors = listOf(Color(0xFF0A0E14), Color(0xFF111820))
                )
            )
    ) {
        Column(modifier = Modifier.fillMaxSize()) {
            // Header bar
            HeaderBar(
                itemName = itemName,
                onItemNameChange = { itemName = it },
                onSuggestName = {
                    if (photoPaths.isNotEmpty()) {
                        scope.launch {
                            isSuggestingName = true
                            try {
                                val apiKey = AppSettings.getGeminiApiKey(context)
                                if (apiKey.isBlank()) {
                                    errorMessage = "No Gemini API key set."
                                    return@launch
                                }
                                val name = suggestItemName(apiKey, photoPaths.first())
                                itemName = name
                            } catch (e: Exception) {
                                errorMessage = e.message
                            } finally {
                                isSuggestingName = false
                            }
                        }
                    }
                },
                isSuggestingName = isSuggestingName,
                isSaving = isSaving,
                onSave = {
                    scope.launch {
                        isSaving = true
                        try {
                            val savedCount = saveAllPhotos(context, photoPaths, adjustments.toMap(), itemName)
                            toastState = ToastState("Saved $savedCount photo(s).")
                        } catch (e: Exception) {
                            toastState = ToastState("Saved to fallback. Check permissions.", isWarning = true)
                        } finally {
                            isSaving = false
                        }
                    }
                },
                onBack = onBack
            )

            // Preview area
            Box(
                modifier = Modifier
                    .weight(1f)
                    .fillMaxWidth(),
                contentAlignment = Alignment.Center
            ) {
                // Main image
                val photoFile = File(photoPaths[selectedIndex])
                val adj = currentAdjustments
                val brightness = 1f + (adj.exposure / 200f)
                val contrast = 1f + (adj.contrast / 200f)

                Box(
                    modifier = Modifier
                        .fillMaxSize()
                        .padding(horizontal = 16.dp, vertical = 8.dp),
                    contentAlignment = Alignment.Center
                ) {
                    AsyncImage(
                        model = photoFile,
                        contentDescription = "Photo ${selectedIndex + 1}",
                        modifier = Modifier
                            .fillMaxSize()
                            .graphicsLayer {
                                scaleX = adj.scale.coerceIn(0.1f, 2f)
                                scaleY = adj.scale.coerceIn(0.1f, 2f)
                            },
                        contentScale = ContentScale.Fit
                    )

                    // Brightness/contrast overlay simulation via semi-transparent layer
                    if (adj.exposure != 0f || adj.contrast != 0f) {
                        Box(
                            modifier = Modifier
                                .fillMaxSize()
                                .graphicsLayer {
                                    // Use alpha to simulate brightness adjustments
                                    alpha = if (adj.exposure > 0) {
                                        (adj.exposure / 400f).coerceIn(0f, 0.3f)
                                    } else {
                                        (-adj.exposure / 400f).coerceIn(0f, 0.3f)
                                    }
                                }
                                .background(
                                    if (adj.exposure > 0) Color.White else Color.Black
                                )
                        )
                    }

                    // Processing overlay
                    if (isFilterProcessing) {
                        Box(
                            modifier = Modifier
                                .fillMaxSize()
                                .background(Color.Black.copy(alpha = 0.6f)),
                            contentAlignment = Alignment.Center
                        ) {
                            Column(horizontalAlignment = Alignment.CenterHorizontally) {
                                CircularProgressIndicator(
                                    modifier = Modifier.size(48.dp),
                                    color = Primary,
                                    strokeWidth = 3.dp
                                )
                                Spacer(Modifier.height(12.dp))
                                Text(
                                    "AI Processing...",
                                    color = Color.White,
                                    fontFamily = FontFamily.Monospace,
                                    fontSize = 14.sp,
                                    fontWeight = FontWeight.Bold
                                )
                            }
                        }
                    }
                }

                // Photo counter
                if (photoPaths.size > 1) {
                    Text(
                        "${selectedIndex + 1} / ${photoPaths.size}",
                        color = Color.White.copy(alpha = 0.5f),
                        fontSize = 11.sp,
                        fontFamily = FontFamily.Monospace,
                        modifier = Modifier
                            .align(Alignment.TopEnd)
                            .padding(16.dp)
                            .background(Color.Black.copy(alpha = 0.5f), RoundedCornerShape(8.dp))
                            .padding(horizontal = 8.dp, vertical = 4.dp)
                    )
                }
            }

            // Thumbnail strip
            if (photoPaths.size > 1) {
                ThumbnailStrip(
                    photoPaths = photoPaths,
                    selectedIndex = selectedIndex,
                    onSelect = { selectedIndex = it },
                    modifier = Modifier.fillMaxWidth()
                )
            }

            // Bottom control panel
            BottomControlPanel(
                activeTab = activeTab,
                onTabChange = { activeTab = it },
                applyToAll = applyToAll,
                onApplyToAllChange = { applyToAll = it },
                adjustments = currentAdjustments,
                onAdjustmentsChange = { newAdj -> updateAdjustments(newAdj) },
                onApplyFilter = { filter ->
                    scope.launch {
                        isFilterProcessing = true
                        try {
                            val apiKey = AppSettings.getGeminiApiKey(context)
                            if (apiKey.isBlank()) {
                                errorMessage = "No Gemini API key set."
                                return@launch
                            }
                            val result = applyAiFilter(context, apiKey, photoPaths[selectedIndex], filter)
                            // Replace the file with the processed result
                            val outputFile = File(photoPaths[selectedIndex])
                            FileOutputStream(outputFile).use { fos ->
                                result.compress(Bitmap.CompressFormat.JPEG, 95, fos)
                            }
                            adjustments[selectedIndex] = currentAdjustments.copy(appliedFilter = filter)
                        } catch (e: Exception) {
                            errorMessage = e.message ?: "AI filter failed"
                        } finally {
                            isFilterProcessing = false
                        }
                    }
                },
                isFilterProcessing = isFilterProcessing
            )
        }

        // Generate listing button (floating)
        if (!hasGenerated) {
            FloatingActionButton(
                onClick = {
                    scope.launch {
                        isGenerating = true
                        errorMessage = null
                        try {
                            val apiKey = AppSettings.getGeminiApiKey(context)
                            if (apiKey.isBlank()) {
                                errorMessage = "No Gemini API key set. Go to Settings → AI Configuration."
                                return@launch
                            }
                            val result = generateListingDescription(apiKey, photoPaths.first())
                            generatedTitle = result.title
                            generatedDescription = result.description
                            generatedPrice = result.suggestedPrice
                            if (itemName.isBlank()) itemName = result.title
                            hasGenerated = true
                        } catch (e: Exception) {
                            errorMessage = e.message ?: "Failed to generate listing"
                        } finally {
                            isGenerating = false
                        }
                    }
                },
                modifier = Modifier
                    .align(Alignment.BottomEnd)
                    .padding(end = 16.dp, bottom = 16.dp),
                containerColor = Primary,
                contentColor = Color(0xFF005A3C)
            ) {
                if (isGenerating) {
                    CircularProgressIndicator(
                        modifier = Modifier.size(24.dp),
                        strokeWidth = 2.dp,
                        color = Color(0xFF005A3C)
                    )
                } else {
                    Icon(Icons.Default.AutoAwesome, "Generate Listing")
                }
            }
        }

        // Generated listing overlay
        if (hasGenerated) {
            ListingOverlay(
                title = generatedTitle,
                onTitleChange = { generatedTitle = it },
                price = generatedPrice,
                onPriceChange = { generatedPrice = it },
                description = generatedDescription,
                onDescriptionChange = { generatedDescription = it },
                errorMessage = errorMessage,
                isGenerating = isGenerating,
                onRegenerate = {
                    scope.launch {
                        isGenerating = true
                        errorMessage = null
                        try {
                            val apiKey = AppSettings.getGeminiApiKey(context)
                            if (apiKey.isBlank()) {
                                errorMessage = "No Gemini API key set."
                                return@launch
                            }
                            val result = generateListingDescription(apiKey, photoPaths.first())
                            generatedTitle = result.title
                            generatedDescription = result.description
                            generatedPrice = result.suggestedPrice
                        } catch (e: Exception) {
                            errorMessage = e.message ?: "Failed to generate listing"
                        } finally {
                            isGenerating = false
                        }
                    }
                },
                onDismiss = { hasGenerated = false },
                context = context
            )
        }

        // Error toast
        errorMessage?.let { error ->
            LaunchedEffect(error) {
                delay(5000)
                errorMessage = null
            }
        }

        // Error bar
        errorMessage?.let { error ->
            Box(
                modifier = Modifier
                    .align(Alignment.BottomCenter)
                    .padding(bottom = 80.dp)
                    .padding(horizontal = 32.dp)
            ) {
                Text(
                    error,
                    color = MaterialTheme.colorScheme.error,
                    fontSize = 12.sp,
                    fontFamily = FontFamily.Monospace,
                    modifier = Modifier
                        .background(Color(0xFF1A0000), RoundedCornerShape(8.dp))
                        .border(1.dp, MaterialTheme.colorScheme.error.copy(alpha = 0.3f), RoundedCornerShape(8.dp))
                        .padding(12.dp)
                )
            }
        }

        // Toast notification
        AnimatedVisibility(
            visible = toastState.isVisible,
            enter = fadeIn() + slideInVertically(initialOffsetY = { it }),
            exit = fadeOut() + slideOutVertically(targetOffsetY = { it }),
            modifier = Modifier
                .align(Alignment.BottomCenter)
                .padding(bottom = 24.dp)
        ) {
            Text(
                toastState.message,
                color = Color.White,
                fontSize = 14.sp,
                fontFamily = FontFamily.Monospace,
                modifier = Modifier
                    .background(
                        if (toastState.isWarning) Color(0xFF4A3000).copy(alpha = 0.95f)
                        else SurfaceDark.copy(alpha = 0.95f),
                        RoundedCornerShape(50)
                    )
                    .padding(horizontal = 24.dp, vertical = 12.dp)
            )
        }
    }
}

