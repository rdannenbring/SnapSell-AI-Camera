                Spacer(Modifier.width(4.dp))
                Text("Finish", fontFamily = FontFamily.Monospace, fontSize = 12.sp, fontWeight = FontWeight.Bold)
            }
        }
    }
}

@Composable
private fun ThumbnailStrip(
    photoPaths: List<String>,
    selectedIndex: Int,
    onSelect: (Int) -> Unit,
    modifier: Modifier = Modifier
) {
    Row(
        modifier = modifier
            .horizontalScroll(rememberScrollState())
            .padding(horizontal = 16.dp, vertical = 8.dp),
        horizontalArrangement = Arrangement.spacedBy(8.dp)
    ) {
        photoPaths.forEachIndexed { index, path ->
            val isSelected = index == selectedIndex
            AsyncImage(
                model = File(path),
                contentDescription = "Thumb ${index + 1}",
                modifier = Modifier
                    .size(56.dp)
                    .clip(RoundedCornerShape(8.dp))
                    .then(
                        if (isSelected) Modifier.border(2.dp, Color.White, RoundedCornerShape(8.dp))
                        else Modifier.border(1.dp, Color.White.copy(alpha = 0.1f), RoundedCornerShape(8.dp))
                    )
                    .then(if (!isSelected) Modifier.graphicsLayer { alpha = 0.5f } else Modifier)
                    .clickable { onSelect(index) },
                contentScale = ContentScale.Crop
            )
        }
    }
}

@Composable
private fun BottomControlPanel(
    activeTab: EditorTab,
    onTabChange: (EditorTab) -> Unit,
    applyToAll: Boolean,
    onApplyToAllChange: (Boolean) -> Unit,
    adjustments: PhotoAdjustments,
    onAdjustmentsChange: (PhotoAdjustments) -> Unit,
    onApplyFilter: (AiFilter) -> Unit,
    isFilterProcessing: Boolean
) {
    Column(
        modifier = Modifier
            .fillMaxWidth()
            .background(SurfaceDark)
            .clip(RoundedCornerShape(topStart = 24.dp, topEnd = 24.dp))
    ) {
        // Tab row + Apply to All toggle
        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(horizontal = 16.dp, vertical = 8.dp),
            horizontalArrangement = Arrangement.SpaceBetween,
            verticalAlignment = Alignment.CenterVertically
        ) {
            // Tab buttons
            Row(horizontalArrangement = Arrangement.spacedBy(4.dp)) {
                EditorTab.entries.forEach { tab ->
                    Column(
                        horizontalAlignment = Alignment.CenterHorizontally,
                        modifier = Modifier
                            .clip(RoundedCornerShape(8.dp))
                            .clickable { onTabChange(tab) }
                            .background(
                                if (activeTab == tab) Color.White.copy(alpha = 0.1f)
                                else Color.Transparent
                            )
                            .padding(horizontal = 12.dp, vertical = 6.dp)
                    ) {
                        Icon(
                            when (tab) {
                                EditorTab.ADJUST -> Icons.Default.Tune
                                EditorTab.AI_FILTERS -> Icons.Default.AutoAwesome
                                EditorTab.CROP -> Icons.Default.Crop
                            },
                            contentDescription = tab.label,
                            tint = if (activeTab == tab) Color.White else Color.White.copy(alpha = 0.4f),
                            modifier = Modifier.size(18.dp)
                        )
                        Text(
                            tab.label,
                            color = if (activeTab == tab) Color.White else Color.White.copy(alpha = 0.4f),
                            fontSize = 10.sp,
                            fontFamily = FontFamily.Monospace,
                            fontWeight = FontWeight.Bold
                        )
                    }
                }
            }

            // Apply to All toggle
            Row(verticalAlignment = Alignment.CenterVertically) {
                Text(
                    "ALL",
                    color = Color.White.copy(alpha = 0.4f),
                    fontSize = 10.sp,
                    fontFamily = FontFamily.Monospace,
                    fontWeight = FontWeight.Bold
                )
                Spacer(Modifier.width(4.dp))
                Switch(
                    checked = applyToAll,
                    onCheckedChange = onApplyToAllChange,
                    modifier = Modifier.height(20.dp),
                    colors = SwitchDefaults.colors(
                        checkedTrackColor = Primary,
                        checkedThumbColor = Color.White,
                        uncheckedTrackColor = Color.White.copy(alpha = 0.1f),
                        uncheckedThumbColor = Color.White.copy(alpha = 0.5f)
                    )
                )
            }
        }

        // Tab content
        AnimatedContent(
            targetState = activeTab,
            transitionSpec = { fadeIn(tween(200)) togetherWith fadeOut(tween(200)) },
            label = "tab_content"
        ) { tab ->
            when (tab) {
                EditorTab.ADJUST -> AdjustTab(
                    adjustments = adjustments,
                    onAdjustmentsChange = onAdjustmentsChange
                )
                EditorTab.AI_FILTERS -> AiFiltersTab(
                    onApplyFilter = onApplyFilter,
                    isProcessing = isFilterProcessing
                )
                EditorTab.CROP -> CropTab()
            }
        }
    }
}

@Composable
private fun AdjustTab(
    adjustments: PhotoAdjustments,
    onAdjustmentsChange: (PhotoAdjustments) -> Unit
) {
    Column(
        modifier = Modifier
            .fillMaxWidth()
            .padding(horizontal = 16.dp, vertical = 8.dp),
        verticalArrangement = Arrangement.spacedBy(12.dp)
    ) {
        // Exposure
        SliderRow(
            icon = { Icon(Icons.Default.WbSunny, "Exposure", tint = Color.White.copy(alpha = 0.6f), modifier = Modifier.size(18.dp)) },
            label = "EXP",
            value = adjustments.exposure,
            range = -100f..100f,
            onValueChange = { onAdjustmentsChange(adjustments.copy(exposure = it)) }
        )

        // Contrast
        SliderRow(
            icon = { Icon(Icons.Default.Contrast, "Contrast", tint = Color.White.copy(alpha = 0.6f), modifier = Modifier.size(18.dp)) },
            label = "CON",
            value = adjustments.contrast,
            range = -100f..100f,
            onValueChange = { onAdjustmentsChange(adjustments.copy(contrast = it)) }
        )

        // Scale
        SliderRow(
            icon = { Icon(Icons.Default.AspectRatio, "Scale", tint = Color.White.copy(alpha = 0.6f), modifier = Modifier.size(18.dp)) },
            label = "SCL",
            value = (adjustments.scale * 100f) - 100f, // map 0.1-2.0 to -90..100 for display
            range = -90f..100f,
            onValueChange = { onAdjustmentsChange(adjustments.copy(scale = ((it + 100f) / 100f).coerceIn(0.1f, 2f))) },
            displayValue = String.format("%.1f", adjustments.scale)
        )
    }
}

@Composable
private fun SliderRow(
    icon: @Composable () -> Unit,
    label: String,
    value: Float,
    range: ClosedFloatingPointRange<Float>,
    onValueChange: (Float) -> Unit,
    displayValue: String = value.toInt().toString()
) {
    Row(
        modifier = Modifier.fillMaxWidth(),
        verticalAlignment = Alignment.CenterVertically
    ) {
        icon()
        Spacer(Modifier.width(8.dp))
        Slider(
            value = value,
            onValueChange = onValueChange,
            valueRange = range,
            modifier = Modifier.weight(1f),
            colors = SliderDefaults.colors(
                thumbColor = Primary,
                activeTrackColor = Primary,
                inactiveTrackColor = Color.White.copy(alpha = 0.1f)
            )
        )
        Spacer(Modifier.width(8.dp))
        Text(
            displayValue,
            color = Color.White.copy(alpha = 0.6f),
            fontSize = 12.sp,
            fontFamily = FontFamily.Monospace,
            modifier = Modifier.width(40.dp)
        )
    }
}

@Composable
private fun AiFiltersTab(
    onApplyFilter: (AiFilter) -> Unit,
    isProcessing: Boolean
) {
    Row(
        modifier = Modifier
            .fillMaxWidth()
            .padding(horizontal = 16.dp, vertical = 12.dp),
        horizontalArrangement = Arrangement.spacedBy(8.dp)
    ) {
        AiFilter.entries.forEach { filter ->
            Box(
